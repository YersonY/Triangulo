def calcular_area(base, altura):
    """
    Calcula el área de un triángulo dado su base y altura.

    Parámetros:
    base (float): La longitud de la base del triángulo.
    altura (float): La altura del triángulo.

    Devuelve:
    float: El área del triángulo.
    """
    area = (base * altura) / 2
    return area
